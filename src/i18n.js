function normalizeId(value = "") {
  return String(value || "").toLocaleLowerCase().replace("_", "-");
}

export function resolveLocaleId(value) {
  const normalized = normalizeId(value);
  if (localeIds.has(normalized)) return normalized;
  if (aliases.has(normalized)) return aliases.get(normalized);
  const base = normalized.split("-")[0];
  return localeIds.has(base) ? base : DEFAULT_LOCALE;
}

export function detectPreferredLocale(nav = globalThis.navigator) {
  const languages = Array.isArray(nav?.languages) && nav.languages.length
    ? nav.languages
    : [nav?.language].filter(Boolean);
  return resolveLocaleId(languages[0] || DEFAULT_LOCALE);
}

export function getLocale(localeId) {
  return SUPPORTED_LOCALES.find((locale) => locale.id === resolveLocaleId(localeId))
    || SUPPORTED_LOCALES[0];
}

export function t(localeId, key, vars = {}) {
  const id = resolveLocaleId(localeId);
  const message = MESSAGES[id]?.[key] || MESSAGES[DEFAULT_LOCALE][key] || key;
  return Object.entries(vars).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    message,
  );
}

export function getMessageKeys() {
  return Object.keys(baseMessages);
}

export function formatFreshness(localeId, snapshot, now = Date.now()) {
  if (!snapshot?.fetchedAt) return t(localeId, "offlineSchedule");
  const seconds = Math.max(0, Math.round((now - new Date(snapshot.fetchedAt).getTime()) / 1000));
  if (snapshot.stale) return t(localeId, "cachedMinutesAgo", { count: Math.max(1, Math.round(seconds / 60)) });
  if (seconds < 60) return t(localeId, "updatedSecondsAgo", { count: seconds });
  return t(localeId, "updatedMinutesAgo", { count: Math.round(seconds / 60) });
}
